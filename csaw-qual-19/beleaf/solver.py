def array_to_int (arr) :
	output = ""
	for i in arr :
		output += struct.pack('B', i)
	s = struct.unpack('<i', output)[0]
	return s

def array_to_long (arr) :
	output = ""
	for i in arr :
		output += struct.pack('B', i)
	s = struct.unpack('<q', output)[0]
	return s

